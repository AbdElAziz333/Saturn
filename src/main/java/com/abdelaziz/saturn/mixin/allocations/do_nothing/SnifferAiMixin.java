import net.minecraft.world.entity.animal.sniffer.SnifferAi;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Redirect;

@Mixin(SnifferAi.class)
public class SnifferAiMixin {
    @Redirect(
            method = "initIdleActivity",
            at = @At(
                    value = "NEW",
                    target = "(II)Lnet/minecraft/world/entity/ai/behavior/DoNothing;"
            )
    )
    private static DoNothing useStaticFinalReferences(int p_22840_, int p_22841_) {
        return DoNothingConstants.SNIFFER_DO_NOTHING;
    }
}
